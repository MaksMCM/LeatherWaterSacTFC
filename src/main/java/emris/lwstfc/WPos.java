package emris.lwstfc;

public class WPos
{
	private int x;
	private int y;
	private int z;
	private boolean isWater;

	public int getX()
	{
		return x;
	}

	public void setX(int x)
	{
		this.x = x;
	}

	public int getY()
	{
		return y;
	}

	public void setY(int y)
	{
		this.y = y;
	}

	public int getZ()
	{
		return z;
	}

	public void setZ(int z)
	{
		this.z = z;
	}

	public boolean isWater()
	{
		return isWater;
	}

	public void setWater(boolean isWater)
	{
		this.isWater = isWater;
	}
}
